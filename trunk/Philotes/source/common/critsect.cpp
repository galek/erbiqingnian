#include "appcommon.h"


//-----------------------------------------------------------------------------
// CONSTANTS
//-----------------------------------------------------------------------------
unsigned int SPIN_WAIT_INTERVAL	=				512;


//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
void InitCritSectSpinWaitInterval(
	void)
{
	CPU_INFO info;
	ASSERT_RETURN(CPUInfo(info));
	if (info.m_AvailCore <= 1)
	{
		SPIN_WAIT_INTERVAL = 1;
	}
}
