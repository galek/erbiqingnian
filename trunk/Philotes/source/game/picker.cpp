void PickerInit(
	GAME * game)
{
	ASSERT_RETURN(game);
	PickerInit(GameGetMemPool(game), game->m_pPickers);
}


//----------------------------------------------------------------------------
//----------------------------------------------------------------------------
void PickerFree(
	GAME * game)
{
	ASSERT_RETURN(game);
	PickerFree(GameGetMemPool(game), game->m_pPickers);
}


//----------------------------------------------------------------------------
//----------------------------------------------------------------------------
int PickerChoose(
	GAME * game,
	RAND & rand)
{
	return PickerChoose(game->m_pPickers, rand);
}


//----------------------------------------------------------------------------
//----------------------------------------------------------------------------
int PickerChoose(
	GAME * game)
{
	return PickerChoose(game->m_pPickers, game->rand);
}


//----------------------------------------------------------------------------
//----------------------------------------------------------------------------
int PickerChooseRemove(
	GAME * game)
{
	return PickerChooseRemove(game->m_pPickers, game->rand);
}

//----------------------------------------------------------------------------
//----------------------------------------------------------------------------
int PickerChooseRemove(
	GAME * game,
	RAND &rand )
{
	return PickerChooseRemove(game->m_pPickers, rand);
}
//----------------------------------------------------------------------------
//----------------------------------------------------------------------------
int PickerGetCount(
	GAME * game)
{
	PICKER* picker = PickerGetCurrent(game);
	ASSERT_RETVAL(picker, INVALID_INDEX);

	return picker->m_nCurrent;
}
