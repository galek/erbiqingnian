void GameWindowMessageLoop( const GAME_WINDOW & tGameWindow )
{
	MSG tMsg;
	while ( PeekMessage(&tMsg, tGameWindow.m_hWnd, 0, 0, PM_NOREMOVE) )
	{
		BOOL bRet = GetMessage(&tMsg, NULL, 0, 0);
		ASSERTV_RETURN( bRet != -1, "Catastrophic error in GetMessage() : %d", GetLastError() );
		if ( ! bRet )
			break;

		TranslateMessage(&tMsg);
		DispatchMessage(&tMsg);
	} 
}

