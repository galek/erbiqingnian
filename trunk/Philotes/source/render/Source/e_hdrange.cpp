PRESULT e_SRGBToLinear( VECTOR * pvVector )
{
	ASSERT_RETFAIL( pvVector );
	pvVector->fX = pow( pvVector->fX, 2.2f );
	pvVector->fY = pow( pvVector->fY, 2.2f );
	pvVector->fZ = pow( pvVector->fZ, 2.2f );
	return S_OK;
}