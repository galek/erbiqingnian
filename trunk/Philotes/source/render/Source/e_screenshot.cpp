#include "e_screenshot.h"

//----------------------------------------------------------------------------
// DEFINES
//----------------------------------------------------------------------------

//----------------------------------------------------------------------------
// GLOBALS
//----------------------------------------------------------------------------

BOOL gbTickUpdated = FALSE;
BOOL gbDrawUpdated = FALSE;
BOOL gbScreenshotCompress = FALSE;


//----------------------------------------------------------------------------
// FORWARD DECLARATIONS
//----------------------------------------------------------------------------

//----------------------------------------------------------------------------
// IMPLEMENTATION
//----------------------------------------------------------------------------

void e_MarkGameTick()
{
	gbTickUpdated = TRUE;
}

//-------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------

void e_MarkGameDraw()
{
	gbDrawUpdated = TRUE;
}

//-------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------

BOOL e_ToggleScreenshotQuality()
{
	gbScreenshotCompress = !gbScreenshotCompress;
	return ! e_GetScreenshotCompress();  // TRUE if high quality
}

//-------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------

BOOL e_GetScreenshotCompress()
{
	return gbScreenshotCompress;
}

